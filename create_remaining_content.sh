# Sleep Routines and Light Exposure

Sleep and mood are closely connected.

## Sleep Hygiene Basics
- **Consistent Schedule**: Same bedtime and wake time every day
- **Wind-Down Routine**: 30 minutes of calming activities before bed
- **Cool, Dark, Quiet Room**: Aim for 65-68°F
- **No Screens**: Stop using phones/tablets 1 hour before bed

## Light Exposure
- **Morning Light**: Get 15-30 minutes of bright light (ideally sunlight) within 1 hour of waking
- **Dim Evening Light**: Reduce light exposure 2-3 hours before bed

## If You Can't Sleep
- Don't lie in bed awake for more than 20 minutes
- Get up and do something calm (read, listen to quiet music)
- Go back to bed when you feel sleepy

> [!TIP]
> Light exposure is one of the most powerful (and free!) ways to regulate mood and sleep.
EOF

