# Behavioral Activation for Depression

Depression makes you want to withdraw. Behavioral activation means doing things even when you don't feel like it.

## The Activity-Mood Connection
When you're depressed, you stop doing things you enjoy, which makes you feel worse, which makes you do even less. It's a cycle.

## Break the Cycle
1. **Make a list** of activities you used to enjoy
2. **Start small**: Pick one easy activity
3. **Schedule it**: Put it on your calendar
4. **Do it anyway**: Even if you don't feel like it
5. **Notice**: How did you feel after?

## Examples of Activating Activities
- Call a friend
- Take a short walk
- Listen to music
- Cook a favorite meal
- Work on a hobby

> [!IMPORTANT]
> You don't need to wait until you "feel like it." Action comes first, mood improves second.
EOF

