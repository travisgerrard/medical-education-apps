# Staying Well and Preventing Relapse

Recovery isn't just about feeling better—it's about staying better.

## Warning Signs of Relapse
- Return of old symptoms (sadness, worry, sleep problems)
- Loss of interest in activities
- Withdrawing from friends
- Skipping medication or therapy

## Prevention Strategies
- **Continue Treatment**: Even when you feel good
- **Maintain Healthy Habits**: Exercise, sleep, social connection
- **Manage Stress**: Use your coping skills
- **Monitor Symptoms**: Use PHQ-9 or GAD-7 monthly

## Create a Wellness Plan
Write down:
- What helps you stay well
- Early warning signs
- Who to contact if symptoms return
- Emergency contacts

> [!IMPORTANT]
> Having a relapse doesn't mean you failed. It means you need to adjust your treatment plan.
