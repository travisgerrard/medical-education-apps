# Worry Management and Thought Reframing

You can't stop anxious thoughts, but you can change how you respond to them.

## Recognize Thinking Traps
- **All-or-Nothing**: "I made one mistake, I'm a total failure"
- **Catastrophizing**: "This will be a disaster"
- **Mind Reading**: "They think I'm stupid"

## Challenge the Thought
1. What's the evidence for this thought?
2. What's the evidence against it?
3. What would I tell a friend thinking this?
4. What's a more balanced thought?

## Worry Time
- Schedule 15 minutes a day to worry
- When worries come up outside that time, write them down for later
- During worry time, problem-solve what you can and let go of what you can't control

> [!NOTE]
> The goal isn't to eliminate all negative thoughts. It's to reduce their power over you.
EOF

