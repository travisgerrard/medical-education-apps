# How Clinicians Diagnose Anxiety and Depression

There's no blood test for depression or anxiety. Diagnosis is based on symptoms.

## The Diagnostic Process
1. **Symptom Review**: Your provider asks about specific symptoms
2. **Duration**: How long have you felt this way?
3. **Severity**: How much do symptoms interfere with your life?
4. **Medical History**: Rule out physical causes (thyroid, anemia, etc.)

## Screening Tools
- **PHQ-9** for depression
- **GAD-7** for anxiety

Scores help quantify severity but don't diagnose alone.

## Differential Diagnosis
Your provider will also consider:
- Bipolar disorder (if you've had manic episodes)
- PTSD (if you've experienced trauma)
- Medical conditions that mimic depression (thyroid disease)

> [!NOTE]
> An accurate diagnosis guides the right treatment.
EOF

