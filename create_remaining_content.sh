# Breathing and Grounding Skills

When anxiety spikes, grounding techniques bring you back to the present moment.

## 4-7-8 Breathing
1. Breathe in through your nose for 4 counts
2. Hold for 7 counts
3. Breathe out through your mouth for 8 counts
4. Repeat 4 times

## 5-4-3-2-1 Grounding
Notice:
- 5 things you can see
- 4 things you can touch
- 3 things you can hear
- 2 things you can smell
- 1 thing you can taste

## Box Breathing
1. Breathe in for 4
2. Hold for 4
3. Breathe out for 4
4. Hold for 4

> [!TIP]
> Practice these when you're calm so they're easier to use during a panic attack.
EOF

