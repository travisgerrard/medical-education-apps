# Alcohol, Cannabis, Caffeine, and Mood

What you consume affects your mental health.

## Alcohol
- Depresses the central nervous system
- May temporarily reduce anxiety but worsens depression over time
- Interferes with sleep quality
- Interacts with antidepressants

**Recommendation**: Limit to 1 drink per day for women, 2 for men. Consider avoiding entirely during treatment.

## Cannabis
- May temporarily reduce anxiety for some, increase it for others
- Can worsen depression with chronic use
- Reduces motivation (not helpful for depression)

## Caffeine
- Can increase anxiety and panic attacks
- Disrupts sleep if consumed after 2 PM
- Withdrawal causes headaches and irritability

**Recommendation**: Limit to 1-2 cups of coffee in the morning. Avoid if you have anxiety.

> [!WARNING]
> Using alcohol or drugs to cope with symptoms is a red flag. Talk to your doctor.
EOF

