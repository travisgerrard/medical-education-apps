# Follow-Up Schedules and Measurement-Based Care

Regular check-ins help ensure treatment is working.

## Typical Follow-Up Schedule
- **First Month on Medication**: Every 1-2 weeks
- **Months 2-3**: Every 3-4 weeks
- **After Stabilization**: Every 1-3 months

## Measurement-Based Care
Your provider may ask you to complete PHQ-9 or GAD-7 at each visit to:
- Track progress objectively
- Decide if treatment needs adjustment
- Catch early signs of relapse

## What to Report
- Changes in symptoms (better or worse)
- Side effects
- Life stressors
- Suicidal thoughts

> [!IMPORTANT]
> Don't wait until your appointment if you're having suicidal thoughts or a crisis. Call or go to the ER.
EOF

