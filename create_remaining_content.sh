# How to Talk About Medicines and Stopping Safely

Good communication about medication prevents problems.

## Be Honest About
- Side effects you're experiencing
- Whether you're taking the medication as prescribed
- Other medications or supplements
- Alcohol or drug use

## Questions to Ask Your Doctor
- Why this medication?
- What side effects should I watch for?
- When should I start feeling better?
- How long will I need to take this?
- What happens if I miss a dose?

## If You Want to Stop
**Don't stop on your own.** Talk to your doctor. They will:
- Assess if you're ready
- Create a tapering schedule
- Plan for monitoring after stopping

> [!WARNING]
> Stopping antidepressants suddenly can cause withdrawal symptoms and increase the risk of relapse.
